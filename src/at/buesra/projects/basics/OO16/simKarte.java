package at.buesra.projects.basics.OO16;

public class simKarte {
    private int ID;
    private String Telefonnumber;

    public simKarte(int ID, String telefonnumber) {
        this.ID = ID;
        Telefonnumber = telefonnumber;
    }

    public int getID() {
        return ID;
    }

    public String getTelefonnumber() {
        return Telefonnumber;
    }



}
